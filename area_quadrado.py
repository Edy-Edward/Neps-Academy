#Entrada de dados
L = int(input("Informe um número para o cálculo da área de um quadrado: "))

#Para realizar o cálculo da área de um objeto geralmente é feito altura x base.
#Porém, como se trata de um quadrado onde sua altura e base são iguais podemos simplesmente elevar qualquer um de seus lados ao quadrado
area = L**2

#Saída
print(area)